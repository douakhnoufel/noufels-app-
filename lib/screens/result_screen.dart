class ResultScreen extends StatelessWidget {
  final Uint8List imageBytes;
  final DetectionResult result;
  final Map<String, double> allProbabilities;

  const ResultScreen({
    super.key,
    required this.imageBytes,
    required this.result,
    required this.allProbabilities,
  });

  Future<void> _shareReport(BuildContext context) async {
    try {
      final tempDir = await getTemporaryDirectory();
      final file = await File('${tempDir.path}/plant_scan.jpg').create();
      await file.writeAsBytes(imageBytes);

      final String reportText = '''
🌱 PlantGuard Diagnosis Report
-------------------------------
Result: ${result.label}
Confidence: ${(result.confidence * 100).toStringAsFixed(1)}%
Severity: ${result.severity}

Description:
${result.description}

Recommended Actions:
${result.recommendations.asMap().entries.map((e) => '${e.key + 1}. ${e.value}').join('\n')}

Generated by PlantGuard Smart Monitor
''';

      await Share.shareXFiles(
        [XFile(file.path)],
        text: reportText,
        subject: 'Potato Plant Health Report - ${result.label}',
      );
    } catch (e) {
      if (context.mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Could not share report: $e'), backgroundColor: Colors.redAccent),
        );
      }
    }
  }

  @override
  Widget build(BuildContext context) {
    final colorScheme = Theme.of(context).colorScheme;
    final textTheme = Theme.of(context).textTheme;
    
    return Scaffold(
      backgroundColor: colorScheme.surface,
      body: Stack(
        children: [
          CustomScrollView(
            physics: const BouncingScrollPhysics(),
            slivers: [
              SliverAppBar(
                expandedHeight: 340,
                pinned: true,
                stretch: true,
                backgroundColor: colorScheme.surface,
                surfaceTintColor: Colors.transparent,
                leading: GestureDetector(
                  onTap: () => Navigator.pop(context),
                  child: Container(
                    margin: const EdgeInsets.all(10),
                    decoration: BoxDecoration(
                      color: Colors.black45,
                      borderRadius: BorderRadius.circular(12),
                      border: Border.all(color: Colors.white10),
                    ),
                    child: const Icon(Icons.close, color: Colors.white, size: 20),
                  ),
                ),
                flexibleSpace: FlexibleSpaceBar(
                  stretchModes: const [StretchMode.zoomBackground],
                  background: Stack(
                    fit: StackFit.expand,
                    children: [
                      Image.memory(
                        imageBytes,
                        fit: BoxFit.cover,
                      ).animate().fadeIn(duration: 400.milliseconds),
                      Container(
                        decoration: BoxDecoration(
                          gradient: LinearGradient(
                            begin: Alignment.topCenter,
                            end: Alignment.bottomCenter,
                            colors: [
                              Colors.transparent,
                              colorScheme.surface.withValues(alpha: 0.8),
                              colorScheme.surface,
                            ],
                            stops: const [0.6, 0.9, 1.0],
                          ),
                        ),
                      ),
                      Positioned(
                        bottom: 30,
                        left: 20,
                        right: 20,
                        child: Column(
                          crossAxisAlignment: CrossAxisAlignment.start,
                          children: [
                            Text(
                              'DETECTION RESULT',
                              style: textTheme.labelSmall?.copyWith(
                                color: Colors.white60,
                                letterSpacing: 2,
                                fontWeight: FontWeight.bold,
                              ),
                            ).animate().fadeIn(delay: 200.milliseconds).slideX(begin: -0.1),
                            const SizedBox(height: 8),
                            Text(
                              result.label,
                              style: textTheme.headlineMedium?.copyWith(
                                color: Colors.white,
                                fontWeight: FontWeight.w900,
                              ),
                            ).animate().fadeIn(delay: 300.milliseconds).slideX(begin: -0.1),
                          ],
                        ),
                      ),
                    ],
                  ),
                ),
              ),

              SliverPadding(
                padding: const EdgeInsets.fromLTRB(20, 0, 20, 120),
                sliver: SliverList(
                  delegate: SliverChildListDelegate([
                    _SeverityBadge(result: result)
                        .animate()
                        .fadeIn(delay: 400.milliseconds)
                        .slideY(begin: 0.1),

                    const SizedBox(height: 24),

                    _InsightCard(
                      title: 'Confidence Overview',
                      child: _ConfidenceDonut(result: result),
                    ).animate().fadeIn(delay: 500.milliseconds),

                    const SizedBox(height: 20),

                    _InsightCard(
                      title: 'Class Analysis',
                      child: _ProbabilityList(probs: allProbabilities),
                    ).animate().fadeIn(delay: 600.milliseconds),

                    const SizedBox(height: 20),

                    _InsightCard(
                      title: 'Diagnosis Details',
                      icon: Icons.info_outline_rounded,
                      child: Text(
                        result.description,
                        style: textTheme.bodyMedium?.copyWith(
                          color: colorScheme.onSurfaceVariant,
                          height: 1.6,
                        ),
                      ),
                    ).animate().fadeIn(delay: 700.milliseconds),

                    const SizedBox(height: 20),

                    _InsightCard(
                      title: 'Recommended Actions',
                      icon: Icons.health_and_safety_outlined,
                      child: _RecommendationsList(result: result),
                    ).animate().fadeIn(delay: 800.milliseconds),
                  ]),
                ),
              ),
            ],
          ),

          // Bottom Bar
          Positioned(
            bottom: 0,
            left: 0,
            right: 0,
            child: _BottomActions(
              result: result,
              onShare: () => _shareReport(context),
            ),
          ),
        ],
      ),
    );
  }
}

